Duotas masyvas array(-10, 0, 2, 9, -5). Atspausdinkite masyvo elementus
mažėjimo tvarka.
– Funkcija išmetanti elementą iš masyvo ir grąžinanti naują masyvą:
array_splice(),
– pvz:
– $a = array(2, 4, 8, 16);
– array_splice($a, $i, 1);
– jei $i = 2 tai $a masyvas pasidarys toks (2, 4, 16)
